          upper_percentile = 100 - lower_percentile

          lower_bound = np.percentile(all_tree_preds, lower_percentile, axis=0)
          upper_bound = np.percentile(all_tree_preds, upper_percentile, axis=0)

          return lower_bound, upper_bound

