"""# 4. 옵튜나 튜닝(최적의 하이퍼파라미터 찾기, sbp/dbp 따로 진행)"""

def objective_sbp(trial, X, y, groups):
        # 1. 튜닝할 하이퍼파라미터 후보군
        params = {
                'n_estimators': trial.suggest_int('n_estimators', 50, 150),
                'max_depth': trial.suggest_int('max_depth', 2, 5),
                'min_samples_split': trial.suggest_int('min_samples_split', 4, 10),
                'min_samples_leaf': trial.suggest_int('min_samples_leaf', 2, 5),
                'random_state': 42,
                'n_jobs': -1
        }

        # 2. RobustScaler + Random Forest 파이프라인
        model = make_pipeline(RobustScaler(), RandomForestRegressor(**params))

        # 3. LeaveOneGroupOut
        cv = LeaveOneGroupOut()

        # 4. 모의고사 실행 및 채점
        scores = []
        for train_idx, val_idx in cv.split(X, y, groups):

            # [Step A: 원본 분할]
            X_tr, X_val = X.iloc[train_idx], X.iloc[val_idx]
            y_tr, y_val = y.iloc[train_idx], y.iloc[val_idx]

            # [Step B: 학습]
            model.fit(X_tr, y_tr)

            # [Step C: 채점] 깨끗한 원본 시험지로 예측 및 오차 계산
            preds = model.predict(X_val)
            scores.append(mean_absolute_error(y_val, preds))

        return np.mean(scores)

def objective_dbp(trial, X, y, groups):
    # 1. 튜닝할 하이퍼파라미터 후보군 (DBP는 똑똑해지면 과적합)
    params = {
        'n_estimators': trial.suggest_int('n_estimators', 50, 100),
        'max_depth': trial.suggest_int('max_depth', 1, 3),
        'min_samples_split': trial.suggest_int('min_samples_split', 6, 12), # 가지치기 조건 강화 (기존 4~10)
        'min_samples_leaf': trial.suggest_int('min_samples_leaf', 4, 8),    # 잎사귀에 최소 4~8명을 남김 (기존 2~5)
        'random_state': 42,
        'n_jobs': -1
    }

    # 2. RobustScaler + Random Forest 파이프라인
    model = make_pipeline(RobustScaler(), RandomForestRegressor(**params))

    # 3. LeaveOneGroupOut
    cv = LeaveOneGroupOut()

    # 4. 모의고사 실행 및 채점
    scores = []
    for train_idx, val_idx in cv.split(X, y, groups):
        X_tr, X_val = X.iloc[train_idx], X.iloc[val_idx]
        y_tr, y_val = y.iloc[train_idx], y.iloc[val_idx]

        model.fit(X_tr, y_tr)

        preds = model.predict(X_val)
        scores.append(mean_absolute_error(y_val, preds))

    return np.mean(scores)

print("\n[Optuna] SBP 모델 최적의 파라미터 탐색 중...")
